"""
Evaluation harness for the legal triage pipeline.

Loads a ground-truth JSONL dataset (generated by apps/dataset-generator),
runs each email through the pipeline, and computes KPIs:
  - Lead Precision   = TP / (TP + FP)
  - Lead Recall      = TP / (TP + FN)
  - Spam FPR         = false positive spam rate (non-spam wrongly refused as spam)
  - Mean e2e latency
  - P90 / P95 latency
  - Total cost (USD)

Results are saved to runtime/benchmarks/<run-id>.json.  If an existing baseline
exists at runtime/benchmarks/baseline.json the harness will compare and exit with
code 1 if Lead Precision drops by more than 5 percentage points.
"""

from __future__ import annotations

import hashlib
import json
import os
import subprocess
import sys
from datetime import datetime, timezone
from pathlib import Path
from statistics import mean
from typing import Any

# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------

# Maps ground-truth class → expected terminal_sink produced by router_node
_CLASS_TO_SINK: dict[str, str] = {
    "pi_lead":       "NewLead",
    "general_legal": "GeneralLegal",
    "spam":          "Refused-Spam",
    "invoice":       "Refused-Invoice",
    "other":         "Refused-Other",
}

# Ground-truth fields present only in the GT JSONL file
_GT_FIELDS = frozenset({
    "gt_class", "gt_severity", "gt_liability_clarity", "gt_scenario",
    "gt_has_attachment", "gt_jurisdiction", "gt_sol_years", "gt_urgency",
})

_PRECISION_REGRESSION_THRESHOLD = 0.05  # alert if Lead Precision drops by more than 5pp


def _git_commit_hash() -> str:
    """Return the current git commit short hash, or 'unknown' if git unavailable."""
    try:
        result = subprocess.run(
            ["git", "rev-parse", "--short", "HEAD"],
            capture_output=True, text=True, timeout=5
        )
        return result.stdout.strip() or "unknown"
    except Exception:
        return "unknown"


def _strip_gt_fields(record: dict[str, Any]) -> dict[str, Any]:
    """Return a copy of record with all gt_* fields removed."""
    return {k: v for k, v in record.items() if k not in _GT_FIELDS}


def _percentile(values: list[float], pct: float) -> float:
    """Compute the p-th percentile of a sorted list."""
    if not values:
        return 0.0
    sorted_v = sorted(values)
    idx = max(0, int(len(sorted_v) * pct / 100) - 1)
    return sorted_v[idx]


# ---------------------------------------------------------------------------
# Core evaluation
# ---------------------------------------------------------------------------

def run_eval(
    dataset_path: Path,
    *,
    max_records: int | None = None,
    output_dir: Path | None = None,
    save_baseline: bool = False,
    verbose: bool = False,
) -> dict[str, Any]:
    """
    Run the triage pipeline against a ground-truth JSONL dataset.

    Args:
        dataset_path: Path to a JSONL file containing ``gt_class`` and public
            email fields (typically ``emails_demo_gt.jsonl``).
        max_records: Limit evaluation to the first N records (useful for quick
            smoke tests). ``None`` evaluates all records.
        output_dir: Directory where benchmark results are written.  Defaults to
            ``runtime/benchmarks/`` relative to the repo root.
        save_baseline: If True, overwrite ``baseline.json`` with this run's
            results.
        verbose: Print per-record progress to stdout.

    Returns:
        A dict containing all computed KPIs plus metadata.

    Raises:
        SystemExit(1): If Lead Precision drops more than 5pp vs the baseline.
    """
    from legal_triage.graph import get_compiled_graph
    from legal_triage.state import initial_state
    import legal_triage.hitl_queue as hitl_queue_mod

    # Reset HITL queue depth so accumulated depth from prior runs in the same
    # Python process does not trigger false alert thresholds.
    hitl_queue_mod.reset()

    # Resolve output directory
    if output_dir is None:
        # Walk up until we find runtime/ (repo root heuristic)
        here = Path(__file__).resolve()
        for parent in here.parents:
            candidate = parent / "runtime" / "benchmarks"
            if candidate.parent.exists():
                output_dir = candidate
                break
        else:
            output_dir = Path.cwd() / "runtime" / "benchmarks"
    output_dir.mkdir(parents=True, exist_ok=True)

    graph = get_compiled_graph()

    records = []
    with dataset_path.open() as fh:
        for line in fh:
            line = line.strip()
            if line:
                records.append(json.loads(line))

    if max_records is not None:
        records = records[:max_records]

    if not records:
        raise ValueError(f"No records found in {dataset_path}")

    # -----------------------------------------------------------------------
    # Inference loop
    # -----------------------------------------------------------------------
    results: list[dict[str, Any]] = []

    for i, record in enumerate(records):
        gt_class = record.get("gt_class", "other")
        expected_sink = _CLASS_TO_SINK.get(gt_class, "Refused-Other")

        # Strip GT fields so the pipeline is blind to ground truth
        public_record = _strip_gt_fields(record)

        state = initial_state(
            email_id=public_record.get("email_id", f"eval-{i:05d}"),
            raw_email=public_record.get("body", ""),
            attachments=public_record.get("attachments", []),
        )
        # Pre-set human_decision so HITL gate never blocks in eval
        state["human_decision"] = "approve"

        try:
            output = graph.invoke(state)
        except Exception as exc:
            if verbose:
                print(f"  [ERROR] record {i}: {exc}", file=sys.stderr)
            results.append({
                "email_id": state["email_id"],
                "gt_class": gt_class,
                "expected_sink": expected_sink,
                "predicted_sink": None,
                "correct": False,
                "error": str(exc),
                "latency_ms": 0,
                "cost_usd": 0.0,
            })
            continue

        predicted_sink = output.get("terminal_sink")
        correct = predicted_sink == expected_sink
        latency_ms = output.get("total_latency_ms", 0)
        cost_usd = output.get("total_cost_usd", 0.0)

        if verbose:
            status = "✓" if correct else "✗"
            print(f"  [{status}] {state['email_id']:20s}  gt={gt_class:15s}  "
                  f"pred={predicted_sink or 'None':20s}  {latency_ms:4d}ms")

        results.append({
            "email_id": state["email_id"],
            "gt_class": gt_class,
            "expected_sink": expected_sink,
            "predicted_sink": predicted_sink,
            "correct": correct,
            "error": None,
            "latency_ms": latency_ms,
            "cost_usd": cost_usd,
        })

    # -----------------------------------------------------------------------
    # KPI computation
    # -----------------------------------------------------------------------
    pi_records  = [r for r in results if r["gt_class"] == "pi_lead"]
    non_pi      = [r for r in results if r["gt_class"] != "pi_lead"]

    tp = sum(1 for r in pi_records  if r["predicted_sink"] == "NewLead")
    fp = sum(1 for r in non_pi      if r["predicted_sink"] == "NewLead")
    fn = sum(1 for r in pi_records  if r["predicted_sink"] != "NewLead")

    precision = tp / (tp + fp) if (tp + fp) > 0 else 0.0
    recall    = tp / (tp + fn) if (tp + fn) > 0 else 0.0

    # Spam FPR: non-spam items incorrectly routed to Refused-Spam
    non_spam       = [r for r in results if r["gt_class"] != "spam"]
    false_spam     = sum(1 for r in non_spam if r["predicted_sink"] == "Refused-Spam")
    spam_fpr       = false_spam / len(non_spam) if non_spam else 0.0

    latencies     = [r["latency_ms"] for r in results if r["latency_ms"] > 0]
    mean_latency  = mean(latencies) if latencies else 0.0
    p90_latency   = _percentile(latencies, 90)
    p95_latency   = _percentile(latencies, 95)
    total_cost    = sum(r["cost_usd"] for r in results)

    accuracy      = sum(1 for r in results if r["correct"]) / len(results) if results else 0.0

    kpis: dict[str, Any] = {
        "lead_precision":   round(precision,    4),
        "lead_recall":      round(recall,        4),
        "spam_fpr":         round(spam_fpr,      4),
        "accuracy":         round(accuracy,      4),
        "mean_latency_ms":  round(mean_latency,  1),
        "p90_latency_ms":   round(p90_latency,   1),
        "p95_latency_ms":   round(p95_latency,   1),
        "total_cost_usd":   round(total_cost,    6),
        "n_records":        len(results),
        "n_errors":         sum(1 for r in results if r["error"]),
        "tp": tp, "fp": fp, "fn": fn,
    }

    run_id = datetime.now(tz=timezone.utc).strftime("%Y%m%dT%H%M%SZ")
    run_report: dict[str, Any] = {
        "run_id":      run_id,
        "commit":      _git_commit_hash(),
        "dataset":     str(dataset_path),
        "llm_tier":    os.environ.get("LLM_TIER", "tier3"),
        "timestamp":   run_id,
        "kpis":        kpis,
        "per_record":  results,
    }

    # -----------------------------------------------------------------------
    # Persist results
    # -----------------------------------------------------------------------
    run_file    = output_dir / f"{run_id}.json"
    latest_file = output_dir / "latest.json"

    run_file.write_text(json.dumps(run_report, indent=2))
    latest_file.write_text(json.dumps(run_report, indent=2))

    if save_baseline:
        (output_dir / "baseline.json").write_text(json.dumps(run_report, indent=2))

    # -----------------------------------------------------------------------
    # Regression check
    # -----------------------------------------------------------------------
    baseline_file = output_dir / "baseline.json"
    if baseline_file.exists() and not save_baseline:
        baseline = json.loads(baseline_file.read_text())
        baseline_precision = baseline.get("kpis", {}).get("lead_precision", 0.0)
        delta = precision - baseline_precision
        if delta < -_PRECISION_REGRESSION_THRESHOLD:
            print(
                f"\n[REGRESSION] Lead Precision dropped {abs(delta)*100:.1f}pp "
                f"vs baseline ({baseline_precision:.3f} → {precision:.3f}).\n"
                f"Baseline run: {baseline.get('run_id', 'unknown')}",
                file=sys.stderr,
            )
            sys.exit(1)

    return run_report
